document.addEventListener("keydown", function (KeyboardEvent) {
  // I listen all my document("HTML")
  if (KeyboardEvent.keyCode == 65) {
    document.getElementById(65).play();
    var element = document.querySelector('[data-key="65"]');
    element.classList.add("playing");
    const myTime = setTimeout(remove, 200);
    function remove() {
      element.classList.remove("playing");
    }
  }
});
